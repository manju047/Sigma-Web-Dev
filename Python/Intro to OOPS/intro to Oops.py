# Why OOPS concept ?
"""
==> Till now we have used the structured programming language.And ==> from now we are going to use the object oriented programming.
==> By using oops concepts it will provides the data security , code reusability , data hiding , encapsulation , polymorphisim etc..
==> Instead of making the programs very complex the python introduces the oops concept.
==> The basic idea of object-oriented programming (OOP) in Python is to use 'classes' and 'objects' to represent real-world concepts and entities.
==> A class is a blueprint or template for creating objects. It defines the properties and methods that an object of that class will have. 
==> An object is an instance of a class, and it contains its own data and methods.

==> One of the key features of OOP in Python is encapsulation, which means (combining both data and methods into a single unit i.e EX - Class ) that the internal state of an object is hidden and can only be accessed or modified through the object's methods. This helps to protect the object's data and prevent it from being modified in unexpected ways.

==> Another key feature of OOP in Python is inheritance, which creating new classes by inheriting the properties and methods of an existing class. This allows for code reuse and makes it easy to create new classes that have similar functionality to existing classes.

==> Polymorphism is also supported in Python, which means that objects of different classes can be treated as if they were objects of a common class. This allows for greater flexibility in code and makes it easier to write code that can work with multiple types of objects.
 """